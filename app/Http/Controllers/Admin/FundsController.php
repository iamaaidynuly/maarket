     * @param  int  $id
     * @return \Illuminate\Http\RedirectResponse
     */
    public function update(Request $request, $id)
    {
        $funds = Funds::find($id);
        $funds->update([
            'name'  =>  $request['name'],
            'min_count'  =>  $request['min_count'],
            'bonus'  =>  $request['bonus'],
            'type'  =>  $request['type'],
        ]);

        return redirect()->route('funds.index')->with('success', 'Успешно изменено');
    }

